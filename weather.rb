weather = "cold"

case weather
when "sunny"
  puts "Put them shorts on!"
when "rainy"
  puts "Don't forget the umbrella!"
when "cloudy"
  puts "Leave the sunglasses at home"
when "foggy"
  puts "What out for Jack the Ripper!"
when "cold"
  puts "Layers...it's all about layers"
else
  puts "Whatever it is, you're not prepared"
end
